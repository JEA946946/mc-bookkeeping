class Command(BaseCommand):
    help = "Seed account types and chart of accounts (Manager.io production data)"

    def handle(self, *args, **options):
        # Create account types
        type_map = {}
        for t in ACCOUNT_TYPES:
            obj, created = AccountType.objects.get_or_create(
                name=t["name"],
                defaults={
                    "normal_balance": t["normal_balance"],
                    "display_order": t["display_order"],
                },
            )
            type_map[t["name"]] = obj
            if created:
                self.stdout.write(f"  Created account type: {obj.name}")

        # Create accounts (without parents first)
        created_count = 0
        for code, name, type_name, _parent in ACCOUNTS:
            _, created = Account.objects.get_or_create(
                code=code,
                defaults={
                    "name": name,
                    "account_type": type_map[type_name],
                },
            )
            if created:
                created_count += 1

        # Set parent relationships
        for code, _name, _type, parent_code in ACCOUNTS:
            if not parent_code:
                continue
            try:
                child = Account.objects.get(code=code)
                parent = Account.objects.get(code=parent_code)
                if child.parent_id != parent.id:
                    child.parent = parent
                    child.save(update_fields=["parent"])
            except Account.DoesNotExist:
                pass

        # Ensure default admin user exists
        User = get_user_model()
        if not User.objects.filter(username="admin").exists():
            u = User.objects.create_superuser("admin", "admin@dmc.ma", "admin123")
            u.first_name = "Admin"
            u.last_name = "User"
            u.save()
            self.stdout.write("  Created default admin user (admin / admin123)")

        self.stdout.write(
            self.style.SUCCESS(
                f"Seeded {len(ACCOUNT_TYPES)} account types, {created_count} new accounts"
            )
        )
